public class Lixo {
    private int x,y, quantPontos;
    
    public Lixo(int x,int y,int quantPontos){
        this.quantPontos = quantPontos;
        this.x = x;
        this.y = y;
    }

    public int getquantPontos() {
        return quantPontos;
    }

    public int getY() {
        return y;
    }

    public int getX() {
        return x;
    }
}
